void printFloatArray(float* array, int size)
{
    for(int i = 0; i < size; i++)
    {
        printf("%f\n", array[i]);
    }
}
void printIntArray(int * array, int size)
{
    for(int i = 0; i < size; i++)
    {
        printf("%d\n", array[i]);
    }
}
void printDoubleArray(double *array, int size)
{
    for(int i = 0; i < size; i++)
    {
        printf("%lf\n", array[i]);
    }
}